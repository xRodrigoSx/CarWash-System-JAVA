    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome = jTextField1.getText();
        String sobrenome = jTextField2.getText();
        String sexo = jTextField4.getText();
        String endereco = jTextField3.getText();
        String datanascimento = jFormattedTextField6.getText();
        String telefone = jFormattedTextField7.getText();
        user = new Cliente(nome,sobrenome,sexo,endereco,telefone,datanascimento);
        JOptionPane.showMessageDialog(null,"Cadastro realizado com sucesso");
        user.infos();
        this.tela_principal.gravaUsuario(user);
        this.dispose();
        tela_principal.atualizarUsuario();
        
        Planos_de_Assinatura planos_de_assinatura = new Planos_de_Assinatura();
        planos_de_assinatura.tela_principal = tela_principal;
        planos_de_assinatura.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

